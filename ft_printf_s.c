#include "ft_printf.h"

int	ft_printf_s(char *s)
{
	int	len;

	if (s == 0)
	{
		ft_putstr("(null)");
		return (6);
	}
	ft_putstr(s);
	len = ft_strlen(s);
	return (len);
}
